    private void modifierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modifierMouseClicked
        // TODO add your handling code here:
        String nom_section = (String) listesections.getSelectedItem();
        
        String sql="UPDATE matieres set matiere=?, coeffmatiere=?, idsection=(SELECT idsections from SECTIONS where section=?) where idmatiere=?";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtmatiere.getText());
            pst.setString(2, txtcoef.getText());
            pst.setString(3, nom_section);
            pst.setString(4, txtidmatiere.getText());
            pst.executeUpdate();
            UpdateJTable();
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_modifierMouseClicked

    private void supprimerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supprimerMouseClicked
        // TODO add your handling code here:
        int n = JOptionPane.showConfirmDialog(
                            null, "Confirmer la suppression?",
                            "Attention",
                            JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {
            String sql="delete from matieres where idmatiere=?";
           try{
               pst = conn.prepareStatement(sql);
               pst.setString(1, txtidmatiere.getText());
               pst.execute();
               UpdateJTable();
               
               txtidmatiere.setText("");
               txtmatiere.setText("");
           }

           catch(Exception e){
               JOptionPane.showMessageDialog(null, e);
           }   
       }        
    }//GEN-LAST:event_supprimerMouseClicked

