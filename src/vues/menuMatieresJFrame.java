    private void Fillcombo(String old){
        listesections.removeAllItems();
        listesections.addItem(old);
        String sql="select section from sections where section!=?";
            try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, old);
            rs = pst.executeQuery();
              while(rs.next()){
                  String S = rs.getString("section");
                  listesections.addItem(S);
              }
            }

            catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }   
    }
