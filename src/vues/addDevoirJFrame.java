    private void submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitMouseClicked
        // TODO add your handling code here:


        String nom_section = (String) listesections.getSelectedItem();
        if(txtdevoir.getText().length()==0 || txtcoef.getText().length()==0){

            JOptionPane.showMessageDialog(null, "Il faut renseigner tous les champs");

        }
        else{
        String sql="INSERT INTO devoirs(devoir, idsection, idmatiere, idsemestre, coef) VALUES(?, ?, ?, ?, ?)";
        try {

            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtdevoir.getText());
            pst.setString(2, txt_id_section.getText());
            pst.setString(3, txt_id_matiere.getText());
            pst.setString(4, (String) listesemestres.getSelectedItem());
            pst.setString(5, txtcoef.getText());

            int rs = pst.executeUpdate();
            pst.close();
            conn.close();

            txtdevoir.setText("");
            txtcoef.setText("");
            
            JOptionPane.showMessageDialog(null, "Vous avez correctement ajouté un devoir");

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_submitMouseClicked

