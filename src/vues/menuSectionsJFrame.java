    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        txtniveau.removeAllItems();
        int row = jTable1.getSelectedRow();
        String niveau= (String) jTable1.getModel().getValueAt(row, 2).toString();
     
        txtsection.setText(jTable1.getModel().getValueAt(row, 1).toString());
        txtniveau.addItem(Integer.parseInt(niveau));
        txtid.setText(jTable1.getModel().getValueAt(row, 0).toString());
        

        int i=0;
        for(i=1; i<6; i++){
            if(i!=Integer.parseInt(niveau)){
                
                txtniveau.addItem(i);
            }            
        }
         
       

    }//GEN-LAST:event_jTable1MouseClicked

    private void modifierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modifierMouseClicked
        // TODO add your handling code here:
        //String niveau = (String)txtniveau.getSelectedItem();
       
        String sql="UPDATE sections set section=?, niveau=? where idsections=?";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtsection.getText());
            pst.setInt(2, (int) txtniveau.getSelectedItem());
            pst.setString(3, txtid.getText());
           
            pst.executeUpdate();
            UpdateJTable();

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_modifierMouseClicked

    private void supprimerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supprimerMouseClicked
        // TODO add your handling code here:
        int n = JOptionPane.showConfirmDialog(
                            null, "Confirmer la suppression?",
                            "Attention",
                            JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {

        String sql="delete from sections where idsections=?";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtid.getText());
            pst.execute();
            UpdateJTable();
            
            txtsection.setText("");
            txtniveau.removeAllItems();
            txtid.setText("");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_supprimerMouseClicked

